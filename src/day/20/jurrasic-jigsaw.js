const parseInput = (input) => {
    const tilesArr = input.split('\n\n');
    const tiles = {};
    tilesArr.forEach((tile, index) => {
        const tileData = tile.split('\n');
        const tileIdArr = tileData[0].split('Tile ');
        const tileId = tileIdArr[1].replace(':', '');
        const rightArr = [];
        const leftArr = [];
        const top = tileData[1];
        const bottom = tileData[tileData.length - 1];
        const tileArr = [];
        for (let i = 1; i < tileData.length; i++) {
            tileArr.push(tileData[i].split(''));
            const n = tileData[i].length;
            rightArr.push(tileData[i][n-1]);
            leftArr.push(tileData[i][0]);
        }
        tiles[tileId] = {
            tileInfo: [top,
                rightArr.join(''),
                bottom,
                leftArr.join(''),
                top.split('').reverse().join(''),
                rightArr.reverse().join(''),
                bottom.split('').reverse().join(''),
                leftArr.reverse().join(''),
            ],
            tileArr
        };
    });

    return {tiles};
};

const separateCorners = (tiles) => {
    const borderTileIds = [];
    const cornerTileIds = [];
    const centerTileIds = [];
    const remainingTileIds = [];
    const tileIds = Object.keys(tiles);
    for (let i = 0; i < tileIds.length; i++) {
        let totalMatch = 0;
        const tileId1 = tileIds[i];
        const tile1Info = tiles[tileId1].tileInfo;

        for (let j = 0; j < tile1Info.length; j++) {
            if (tileIds.some(tileId2 => {
                const tileId2Info = tiles[tileId2].tileInfo;
                return tileId1 !== tileId2 && tileId2Info.includes(tile1Info[j]);
            })) {
                totalMatch++;
            }
        }
        totalMatch = totalMatch / 2;
        if (totalMatch > 3) {
            centerTileIds.push(tileIds[i]);
        } else if (totalMatch === 3) {
            borderTileIds.push(tileIds[i]);
        } else if (totalMatch === 2) {
            cornerTileIds.push(tileIds[i]);
        } else {
            remainingTileIds.push(tileIds[i]);
        }
    }

    return {nonCornerTileIds: borderTileIds, cornerTileIds, centerTileId: centerTileIds};
};

const rotateMatrix = (a, dir) => {
    const newMatrix = [];
    if (dir === 'horizontal') {
        for (let i = 0; i < a.length; i++) {
            newMatrix[i] = [];
            let k = 0;
            for (let j = a[i].length - 1; j >= 0; j--) {
                newMatrix[i][k] = a[i][j];
                k++;
            }
        }
    } else if (dir === 'vertical') {
        let k = a[0].length - 1;
        for (let i = 0; i < a.length; i++) {
            newMatrix[k] = [];
            for (let j = 0; j < a[i].length; j++) {
                newMatrix[k][j] = a[i][j];
            }
            k--;
        }
    } else if (dir === 'antiClockwise') {
        let l = 0;
        for (let i = 0; i < a.length; i++) {
            let k = a[0].length - 1;
            for (let j = 0; j < a[i].length; j++) {
                newMatrix[k] = newMatrix[k] ? newMatrix[k] : [];
                newMatrix[k][l] = a[i][j];
                k--;
            }
            l++;
        }
    } else if (dir === 'clockwise') {
        let l = a[0].length - 1;
        for (let i = 0; i < a.length; i++) {
            let k = 0;
            for (let j = 0; j < a[i].length; j++) {
                newMatrix[k] = newMatrix[k] ? newMatrix[k] : [];
                newMatrix[k][l] = a[i][j];
                k++;
            }
            l--;
        }
    }

    return newMatrix;
};

